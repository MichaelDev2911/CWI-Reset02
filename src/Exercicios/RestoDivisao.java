package Exercicios;

public class RestoDivisao {
    public static void main(String[] args) {
        // Variáveis
        int valor1 = 6;
        int valor2 = 3;
        // Calculo
        int restoDivisao = 6 % 3;
        // Imprimir terminal
        System.out.println(" O resto da divisão entre "+ valor1 + " e " + valor2 +" é igual a " + restoDivisao);
    }
}
